# Arithmatic Operator
a = 4
b = 6
c = a+b
print(c)


# Assignment Operator
a = 5-2     # Assign 5-2 to a
print(a)
b = 6
b += 3      # Increment the value of b y 3 and assign it to b
print(b)
d = 6
d -= 3      # Decrement the value of b y 3 and assign it to b
print(d)
e = 6
e *= 3      # Multiply the value of b y 3 and assign it to b
print(e)
f = 6
f /= 3      # Divide the value of b y 3 and assign it to b
print(f)


# Comparison Operator : ==, >, >=, <, != etc.
g = 5<3
print(g)
h = 5>3
print(h)


# Logical Operators
i = True or False

# Truth table of OR 
print("If True or False then", True or False)
print("If False or True then", False or True)
print("If True or False then", True or False)
print("If False or False then", False or False)

# Truth table of AND
print("If True and False then", True and False)
print("If False and True then", False and True)
print("If True and False then", True and False)
print("If False and False then", False and False)

# Truth table of NOT
print(not(True))
print(not(False))
