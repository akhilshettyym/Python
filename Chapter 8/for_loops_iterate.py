# For loop with list
list = [1, 34, 567, 775, 45, 765, 12]
for i in list:
    print(i)
print("For loop using LIST")

# For loop with tuple
tuple = (1, 4 , 765, 43 , 533, 435, 344)
for i in tuple:
    print(i)
print("For loop using TUPLE")

# For loop with string
string = "Akhil"
for i in string:
    print(i)
print("For loop for string")