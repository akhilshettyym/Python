#Dictionary is a collection of keys-value pairs.
# A dictionary is an unordered collection of items. Each item is a key-value pair. Dictionaries are used to store data values in key-value pairs, making them very efficient for looking up information.


# PROPERTIES OF PYTHON DICTIONARIES
# 1. It is unordered.
# 2. It is mutable.
# 3. It is indexed.
# 4. Cannot contain duplicate keys

marks = {
    "Akhil" : 100,
    "Lochan" : 85,
    "Atin" : 85,
    "Lipika" : 90
}

print(marks)
print(type(marks))
print(marks["Akhil"])
print(marks["Lipika"])

